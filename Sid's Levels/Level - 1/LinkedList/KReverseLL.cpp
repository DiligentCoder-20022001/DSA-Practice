struct node *reverse (struct node *head, int k)
{ 
    // Complete this method
    node *cur = head,*next = NULL,*prev = NULL;
    //reverse the first k nodes
    int counter = 0;
    while(cur != NULL && counter < k)
    {
        next = cur->next;
        cur->next = prev;
        prev = cur;
        cur = next;
        counter++;
    }
    //for the next half
    if(next != NULL)
    head->next = reverse(next,k);
    return prev;
    
}
